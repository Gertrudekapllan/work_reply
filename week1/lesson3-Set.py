# Задача 1 Напишите программу, которая запрашивает у пользователя два списка чисел через
# запятую и выводит на экран все элементы, которые содержатся в обоих
# списках, используя множества.
# Пример входных данных:
# Введите первый список: 1,2,3,4,5
# Введите второй список: 3,4,5,6,7
# Пример выходных данных:
# Общие элементы: {3, 4, 5}
def task_1():
    first_list = input("INPUT task_1:").split(",")
    second_list = input("INPUT task_1:").split(",")
    result_1 = set(first_list)
    result_2 = set(second_list)
    print(result_1, result_2)
    come_on = set(first_list).intersection(second_list)
    print(come_on)


task_1()


