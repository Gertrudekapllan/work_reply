# # Задача 2 Напишите программу, которая принимает два списка чисел и выводит на экран все числа,
# # которые есть в первом списке, но отсутствуют во втором списке, используя множества:
# # Пример входных данных:
# # Введите первый список: 1,2,3,4,5
# # Введите второй список: 3,4,5,6,7
# # Пример выходных данных:
# # Результат: {1, 2}
def task_2():
    first_list = input("INPUT task_2:").split(",")
    second_list = input("INPUT task_2:").split(",")
    res = set(first_list).symmetric_difference(second_list)
    print(res)


task_2()


